// SenseActions.cs
// Главный enum файл — все базовые и суб-действия дегустатора

namespace Degustation
{
    // ──────────────────────────────────────────────
    // БАЗОВЫЕ ВОСПРИЯТИЯ (стартовый набор)
    // ──────────────────────────────────────────────
    public enum SenseType
    {
        Taste,      // Вкус
        Vision,     // Зрение
        Touch,      // Чувства / осязание
        Smell,      // Дыхание / обоняние
        Hearing     // Слух
    }

    // ──────────────────────────────────────────────
    // ВКУС — суб-действия
    // ──────────────────────────────────────────────
    public enum TasteAction
    {
        Lick,               // Лизнуть (+стартовый)
        Taste,              // Попробовать (+стартовый)
        HoldOnTongue,       // Подержать на языке (*свиток)
        HoldUnderTongue     // Подержать под языком (*свиток)
    }

    // ──────────────────────────────────────────────
    // ЗРЕНИЕ — суб-действия
    // ──────────────────────────────────────────────
    public enum VisionAction
    {
        Look,               // Посмотреть (+стартовый)
        Squint,             // Прищуриться (*свиток)
        ExamineEach,        // Рассмотреть каждую отдельно (*свиток)
        LookInSunlight      // Посмотреть под солнцем (*свиток)
    }

    // ──────────────────────────────────────────────
    // ОСЯЗАНИЕ — суб-действия
    // ──────────────────────────────────────────────
    public enum TouchAction
    {
        Touch,              // Потрогать (+стартовый)
        MeasureTemp,        // Измерить температуру (*свиток)
        RubBetweenFingers   // Растереть меж пальцами (*свиток)
    }

    // ──────────────────────────────────────────────
    // ОБОНЯНИЕ — суб-действия
    // ──────────────────────────────────────────────
    public enum SmellAction
    {
        Sniff,              // Понюхать (+стартовый)
        DeepSniff           // Занюхнуть (*свиток)
    }

    // ──────────────────────────────────────────────
    // СЛУХ — суб-действия
    // ──────────────────────────────────────────────
    public enum HearingAction
    {
        Listen,             // Послушать (+стартовый)
        ListenVibrations    // Прислушаться к определённым вибрациям (*свиток)
    }

    // ──────────────────────────────────────────────
    // ФЛАГИ: стартовый / требует свитка
    // Используется в ScriptableObject ниже
    // ──────────────────────────────────────────────
    public enum UnlockType
    {
        Starter,    // + доступен с начала
        Scroll      // * открывается свитком навыка
    }
}
